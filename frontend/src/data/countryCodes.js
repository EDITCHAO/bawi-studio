// Fonction pour obtenir les options uniques triĂ©es
export const getCountryCodeOptions = () => {
  const uniqueCountries = [];
  const seenCodes = new Set();

  countryCodes.forEach(item => {
    const key = `${item.code}-${item.country}`;
    if (!seenCodes.has(key)) {
      seenCodes.add(key);
      uniqueCountries.push(item);
    }
  });

  return uniqueCountries.sort((a, b) => {
    return a.country.localeCompare(b.country, 'fr');
  });
};
